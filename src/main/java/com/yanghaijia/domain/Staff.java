package com.yanghaijia.domain;

/**
 * @author yhjzs
 */
public class Staff {
    private Integer id;
    private String workerId;
    private String workerName;
    private String workerBirthday;
    private String workerPhone;
    private String workerEmail;
    private String workerDepartment;
    private String workerOtherNote;
    private String workerRole;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getWorkerId() {
        return workerId;
    }

    public void setWorkerId(String workerId) {
        this.workerId = workerId;
    }

    public String getWorkerName() {
        return workerName;
    }

    public void setWorkerName(String workerName) {
        this.workerName = workerName;
    }

    public String getWorkerBirthday() {
        return workerBirthday;
    }

    public void setWorkerBirthday(String workerBirthday) {
        this.workerBirthday = workerBirthday;
    }

    public String getWorkerPhone() {
        return workerPhone;
    }

    public void setWorkerPhone(String workerPhone) {
        this.workerPhone = workerPhone;
    }

    public String getWorkerEmail() {
        return workerEmail;
    }

    public void setWorkerEmail(String workerEmail) {
        this.workerEmail = workerEmail;
    }

    public String getWorkerDepartment() {
        return workerDepartment;
    }

    public void setWorkerDepartment(String workerDepartment) {
        this.workerDepartment = workerDepartment;
    }

    public String getWorkerOtherNote() {
        return workerOtherNote;
    }

    public void setWorkerOtherNote(String workerOtherNote) {
        this.workerOtherNote = workerOtherNote;
    }

    public String getWorkerRole() {
        return workerRole;
    }

    public void setWorkerRole(String workerRole) {
        this.workerRole = workerRole;
    }
}
